package com.tower.tower.bean;

public class TowerReport {
	private int id;

	private int towerid;

	private int viewcount;

	private int salecount;

	private int cancelcount;

	private int timecount;

	private int rejectcount;

	private int conrejectcount;

	private int pdcount;

	public int getId() {
		return id;
	}

	public void setId(int id) {
		this.id = id;
	}

	public int getTowerid() {
		return towerid;
	}

	public void setTowerid(int towerid) {
		this.towerid = towerid;
	}

	public int getViewcount() {
		return viewcount;
	}

	public void setViewcount(int viewcount) {
		this.viewcount = viewcount;
	}

	public int getSalecount() {
		return salecount;
	}

	public void setSalecount(int salecount) {
		this.salecount = salecount;
	}

	public int getCancelcount() {
		return cancelcount;
	}

	public void setCancelcount(int cancelcount) {
		this.cancelcount = cancelcount;
	}

	public int getTimecount() {
		return timecount;
	}

	public void setTimecount(int timecount) {
		this.timecount = timecount;
	}

	public int getRejectcount() {
		return rejectcount;
	}

	public void setRejectcount(int rejectcount) {
		this.rejectcount = rejectcount;
	}

	public int getConrejectcount() {
		return conrejectcount;
	}

	public void setConrejectcount(int conrejectcount) {
		this.conrejectcount = conrejectcount;
	}

	public int getPdcount() {
		return pdcount;
	}

	public void setPdcount(int pdcount) {
		this.pdcount = pdcount;
	}
	
	
}
